#!/usr/bin/env node
// Generate docs/reference/config.md from the canonical rayact.config JSON Schema,
// so the documented fields can never drift from what the loader validates.
//   node docs/scripts/gen-config-reference.mjs           # write the page
//   node docs/scripts/gen-config-reference.mjs --check    # fail if out of date
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const here = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(here, '../..');
const SCHEMA = path.join(ROOT, 'packages/rayact-dev-server/schema/rayact.config.schema.json');
const OUT = path.join(here, '..', 'reference', 'config.md');

const schema = JSON.parse(fs.readFileSync(SCHEMA, 'utf8'));

function resolveRef(ref) {
  const m = /^#\/\$defs\/(.+)$/.exec(ref);
  return m ? schema.$defs[m[1]] : null;
}

function typeOf(s) {
  if (s.$ref) { const t = resolveRef(s.$ref); return t ? typeOf(t) : 'object'; }
  if (s.enum) return s.enum.map((e) => `\`${e}\``).join(' \\| ');
  if (s.type === 'array') return `${typeOf(s.items)}[]`;
  return s.type ?? 'any';
}

function rows(props, prefix = '') {
  let out = '';
  for (const [key, sub] of Object.entries(props)) {
    if (key === '$schema') continue;
    const name = prefix ? `${prefix}.${key}` : key;
    const desc = (sub.description ?? '').replace(/\|/g, '\\|');
    out += `| \`${name}\` | ${typeOf(sub)} | ${desc} |\n`;
    const obj = sub.$ref ? resolveRef(sub.$ref) : sub;
    if (obj && obj.type === 'object' && obj.properties) {
      out += rows(obj.properties, name);
    }
  }
  return out;
}

const body = `# Configuration: \`rayact.config.json\`

<!-- GENERATED by docs/scripts/gen-config-reference.mjs from rayact.config.schema.json. Do not edit. -->

${schema.description}

Add the \`$schema\` key for editor autocomplete:

\`\`\`json
{
  "$schema": "./node_modules/@rayact/dev-server/schema/rayact.config.schema.json",
  "rayactAppKey": "my-app",
  "entry": "src/App.tsx",
  "platform": "desktop"
}
\`\`\`

## Fields

| Field | Type | Description |
| --- | --- | --- |
${rows(schema.properties)}
> Transforms default to \`dev=false, debug=false, release=true\`. Only record an
> override — e.g. \`"transform": { "bytecode": { "release": false } }\`.
`;

if (process.argv.includes('--check')) {
  const current = fs.existsSync(OUT) ? fs.readFileSync(OUT, 'utf8') : '';
  if (current !== body) {
    console.error('reference/config.md is out of date — run `npm run gen:config`.');
    process.exit(1);
  }
  console.log('config reference up to date.');
} else {
  fs.mkdirSync(path.dirname(OUT), { recursive: true });
  fs.writeFileSync(OUT, body);
  console.log(`Wrote ${path.relative(ROOT, OUT)}`);
}
